local M = {}
 
local function draw_tile(symbol_array, x, y)
	for i = 0, 7 do
		for j = 0, 7 do
			if symbol_array[(i+8*j)+1] == 1 then
				gui.pixel(i+x,j+y,'#FFFF00');
			end	
			if symbol_array[(i+8*j)+1] == 2 then
				gui.pixel(i+x,j+y,'#B9AF00');
			end	
		end
	end
end

local function draw_char(ch, x, y)
	ch = string.upper(ch);
	fontstr = '0123456789ABCDEF';
	for i = 1, 16 do
		if ch == string.sub(fontstr, i, i) then
			draw_tile(d[i], x, y);
		end
	end
end

local function draw_hexstr(str, x, y)
	for i=1, string.len(str) do
		draw_char(string.sub(str, i, i), (i*8)+x, y);
	end
end



M.draw_tile = draw_tile
M.draw_char = draw_char
M.draw_hexstr = draw_hexstr
 
return M